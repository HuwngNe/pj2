    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txttitlebonusedit.getText();
        String price = txtpricebonusedit.getText();
        if (!"".equals(name) && !"".equals(price)) {
            Bonus bonus = new Bonus();
            bonus.setName(name);
            bonus.setPay(price);
            bonus.setId_bonus(iddataad.getIdChooseBonus());
            QuerryBonus.update(bonus);
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập đầy đủ thông tin","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

