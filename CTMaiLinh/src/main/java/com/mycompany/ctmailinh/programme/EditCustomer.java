    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txtnamecustomer.getText();
        String gender = "";
        if (txtmalecustomer.isSelected()) {
            gender = "male";
        } else if (txtfemalecustomer.isSelected()) {
            gender = "female";
        } else {
            gender = "orther";
        }
        
        String checkdate = String.valueOf(txtdatecustomer.getSelectedItem());
        String date = "";
        if (checkdate.length() == 1) {
            date = "0"+checkdate;
        } else {
            date = checkdate;
        }
        String[] checkmonth = String.valueOf(txtmonthcustomer.getSelectedItem()).split(" ");
        String month = "";
        if (checkmonth[1].length() == 1) {
            month = "0"+checkmonth[1];
        } else {
            month = checkmonth[1];
        }
        String year = String.valueOf(txtyearcustomer.getSelectedItem());
        
        String cmnd = txtcmndcustomer.getText();
        String address = txtaddresscustomer.getText();
        String phone = txtphonecustomer.getText();
        if (!"".equals(name) && !"".equals(cmnd) && !"".equals(gender) && !"".equals(address) && !"".equals(phone)) {
            Customer cus = new Customer();
            cus.setFullname(name);
            cus.setGender(gender);
            cus.setID_card(cmnd);
            cus.setBirthday(date+"-"+month+"-"+year);
            cus.setAddress(address);
            cus.setPhone(phone);
            cus.setId(iddataad.getIdChooseCustomer());
            QuerryCustomer.editById(cus);
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập đầy đủ thông tin trước khi sửa","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
