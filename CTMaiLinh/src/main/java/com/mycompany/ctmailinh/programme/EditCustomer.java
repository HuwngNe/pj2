    public EditCustomer() {
        initComponents();
        setLocationRelativeTo(null);
        addComboBoxDate();
        addComboBoxMonth();
        addComboBoxYear();
        Customer cus = QuerryCustomer.find(iddataad.getIdChooseCustomer());
        txtnamecustomer.setText(cus.getFullname());
        if (!"null".equalsIgnoreCase(cus.getAddress())) {
            txtaddresscustomer.setText(cus.getAddress());
        }
        
        if (cus.getBirthday().length() > 4) {
            HandleDate handle = new HandleDate();
            String[] date = cus.getBirthday().split("-");
            txtdatecustomer.setSelectedItem(handle.dateString(date[0]));
            txtmonthcustomer.setSelectedItem("Tháng "+handle.dateString(date[1]));
            txtyearcustomer.setSelectedItem(handle.dateString(date[2]));
        }
        
        if (!"null".equalsIgnoreCase(cus.getGender())) {
            if ("male".equalsIgnoreCase(cus.getGender())) {
                txtmalecustomer.setSelected(true);
            } else if ("female".equalsIgnoreCase(cus.getGender())) {
                txtfemalecustomer.setSelected(true);
            } else {
                txtorthercustomer.setSelected(true);
            }
        }
        
        if (!"null".equalsIgnoreCase(cus.getID_card())) {
            txtcmndcustomer.setText(cus.getAddress());
        }
        
        if (!"null".equalsIgnoreCase(cus.getPhone())) {
            txtphonecustomer.setText(cus.getPhone());
        }
    }

    private void addComboBoxDate() {
        for (int i = 1; i < 32; i++) {
            txtdatecustomer.addItem(String.valueOf(i));
        }
    }

    private void addComboBoxMonth() {
        for (int i = 1; i < 13; i++) {
            txtmonthcustomer.addItem("Tháng "+String.valueOf(i));
        }
    }

    private void addComboBoxYear() {
        for (int i = 2000; i < 2024; i++) {
            txtyearcustomer.addItem(String.valueOf(i));
        }
    }
