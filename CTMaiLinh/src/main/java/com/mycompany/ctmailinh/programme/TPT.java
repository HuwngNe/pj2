    private void showTable() {
        tableModel.setRowCount(0);
        for (Hire h : dataList) {
            tableModel.addRow(new Object[] {
                tableModel.getRowCount() + 1,
                h.getId_hire(),
                h.getNameHire(),
                h.getNamePayment()
            });
        }
    }
