    private void txtoptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtoptionActionPerformed
        int option = txtoption.getSelectedIndex();
        dataList = switch (option) {
            case 0 -> QuerryHire.selectCorrWait();
            case 1 -> QuerryHire.selectCorr();
            default -> QuerryHire.select();
        };
        showTable();
    }//GEN-LAST:event_txtoptionActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String search = txtsearch.getText();
        String regex = "[0-9]+";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(search);
        if (!matcher.find()) {
            JOptionPane.showMessageDialog(rootPane, "Mã phải là số","Lỗi",JOptionPane.ERROR_MESSAGE);
        } else {
            dataList = QuerryHire.findByIdS(Integer.parseInt(search));
            showTable();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

