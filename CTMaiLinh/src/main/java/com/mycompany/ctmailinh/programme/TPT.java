    private void tablehireMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablehireMouseClicked
        int mousechoose = tablehire.getSelectedRow();
        if (mousechoose == -1) {
            return;
        }
        int a = Integer.parseInt(String.valueOf(tablehire.getValueAt(mousechoose, 1)));
        iddataad.setIdChooseHire(a);
    }//GEN-LAST:event_tablehireMouseClicked

