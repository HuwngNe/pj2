    private void showNewData() {
        dataList = QuerryHire.select(idlogin.getIdCurrent());
         
        showTable();
    }
    
    private void showTable() {
        tableModel.setRowCount(0);
        lsStatus = new ArrayList<>();
        for (Hire h : dataList) {
            lsStatus.add(h.getStatus_correct_id());
            tableModel.addRow(new Object[] {
                tableModel.getRowCount() + 1,
                h.getId_hire(),
                h.getNameHire(),
                h.getNamePayment()
            });
        }
    }
