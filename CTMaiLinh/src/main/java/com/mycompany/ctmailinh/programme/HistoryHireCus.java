    private void tablehistoryhireMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablehistoryhireMouseClicked
        int mousechoose = tablehistoryhire.getSelectedRow();
        index = mousechoose;
        if (mousechoose == -1) {
            return;
        }
        int a = Integer.parseInt(String.valueOf(tablehistoryhire.getValueAt(mousechoose, 1)));
        ichoosehire.setIdchoosehire(a);
    }//GEN-LAST:event_tablehistoryhireMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (ichoosehire.getIdchoosehire() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn đơn muốn xem","Lỗi",JOptionPane.ERROR_MESSAGE);
        } else {
            DetailHireCus detailhire = new DetailHireCus();
            detailhire.setVisible(true);
            setVisible(false);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (ichoosehire.getIdchoosehire() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn đơn muốn sửa","Lỗi",JOptionPane.ERROR_MESSAGE);
        } else {
            if (lsStatus.get(index) == 2) {
                JOptionPane.showMessageDialog(rootPane, "Đơn đã được xác nhận không thể sửa chữa","Lỗi",JOptionPane.INFORMATION_MESSAGE);
            } else {
                EditCusHire editcushire = new EditCusHire();
                editcushire.setVisible(true);
                setVisible(false);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

