    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        IdChooseOffice idchooseOff = new IdChooseOffice();
        String position = txtpositionofficeedit.getText();
        
        String checkDate =  String.valueOf(txtdateofficestedit.getSelectedItem());
        String dateSt = "";
        if (checkDate.length() == 1) {
            dateSt = "0"+checkDate;
        } else {
            dateSt = checkDate;
        }
        String[] checkMonth = String.valueOf(txtmonthofficestedit.getSelectedItem()).split(" ");
        String monthSt = "";
        if (checkMonth[1].length() == 1) {
            monthSt = "0"+checkMonth[1];
        } else {
            monthSt = checkMonth[1];
        }
        String yearSt = String.valueOf(txtyearofficestedit.getSelectedItem());
        
        String checkDateEd = String.valueOf(txtdateofficeededit.getSelectedItem());
        String dateEd = "";
        if (checkDateEd.length() == 1) {
            dateEd = "0"+checkDateEd;
        } else {
            dateEd = checkDateEd;
        }
        String[] checkMonthEd = String.valueOf(txtmonthofficeededit.getSelectedItem()).split(" ");
        String monthEd = "";
        if (checkMonthEd[1].length() == 1) {
            monthEd = "0"+checkMonthEd[1];
        } else {
            monthEd = checkMonthEd[1];
        }
        String yearEd = String.valueOf(txtyearofficeededit.getSelectedItem());
        
        if (!position.equals("")) {
            Office office = new Office();
            office.setId_office(idchooseOff.getIdChooseOffice());
            office.setId_employee(idchooseNV.getIdChooseNV());
            office.setPosition(position);
            office.setStart(dateSt+"-"+monthSt+"-"+yearSt);
            if (!checkDateEd.equalsIgnoreCase("1") && !checkMonthEd[1].equalsIgnoreCase("1") && !yearEd.equalsIgnoreCase("2000")) {
                office.setEnd(dateEd+"-"+monthEd+"-"+yearEd);
            }
            QuerryOffice.update(office);
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập thông tin sửa","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

