    private void addComboBoxDate() {
        for (int i = 1; i < 32; i++) {
            txtdateofficestedit.addItem(String.valueOf(i));
            txtdateofficeededit.addItem(String.valueOf(i));
        }
    }

    private void addComboBoxMonth() {
        for (int i = 1; i < 13; i++) {
            txtmonthofficestedit.addItem("Tháng "+String.valueOf(i));
            txtmonthofficeededit.addItem("Tháng "+String.valueOf(i));
        }
    }

    private void addComboBoxYear() {
        for (int i = 2000; i < 2024; i++) {
            txtyearofficestedit.addItem(String.valueOf(i));
            txtyearofficeededit.addItem(String.valueOf(i));
        }
    }
