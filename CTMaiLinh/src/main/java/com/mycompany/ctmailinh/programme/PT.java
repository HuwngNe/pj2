    private void txtsortptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtsortptActionPerformed
        int option = txtsortpt.getSelectedIndex();
        dataList = switch (option) {
            case 0 -> QuerryVehice.selectStatus1();
            case 1 -> QuerryVehice.selectStatus2();
            case 2 -> QuerryVehice.selectSit4();
            case 3 -> QuerryVehice.selectSit8();
            case 4 -> QuerryVehice.selectSit16();
            default -> QuerryVehice.select();
        };
        showTable();
    }//GEN-LAST:event_txtsortptActionPerformed

    private void tablevehiceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablevehiceMouseClicked
        int choosemouse = tablevehice.getSelectedRow();
        if (choosemouse == -1) {
            return;
        }
        int a = (int) tablevehice.getValueAt(choosemouse, 1);
        iddataad.setIdChooseVehice(a);
    }//GEN-LAST:event_tablevehiceMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (iddataad.getIdChooseVehice() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn phương tiện muốn xoá","Lỗi",JOptionPane.ERROR_MESSAGE);
        } else {
            int option = JOptionPane.showConfirmDialog(rootPane,
                        "Bạn có chắc muốn xoá xe này không ?",
                        "Xác nhận",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
            if (option == 0) {
                QuerryHire.deleteById(iddataad.getIdChooseVehice());
                QuerryVehice.delete(iddataad.getIdChooseVehice());
                JOptionPane.showMessageDialog(rootPane, "Xoá thành công");
                showNewData();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

