    private void showTable() {
        tableModel.setRowCount(0);
        
        for (Vehice vehice : dataList) {
            tableModel.addRow(new Object[] {
                tableModel.getRowCount() + 1,
                vehice.getId(),
                vehice.getName(),
                vehice.getNumber_of_seats(),
                vehice.getSit()
            });
        }
    }
