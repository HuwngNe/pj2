    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String[] vehiceChoose = String.valueOf(txtvehicehireedit.getSelectedItem()).split(" - ");
        String[] driverChoose = String.valueOf(txtdriverhireedit.getSelectedItem()).split(" - ");

        String checkdate = String.valueOf(txtdatehireedit.getSelectedItem());
        String date = "";
        if (checkdate.length() == 1) {
            date = "0"+checkdate;
        } else {
            date = checkdate;
        }
        String[] checkmonth = String.valueOf(txtmonthhireedit.getSelectedItem()).split(" ");
        String month = "";
        if (checkmonth[1].length() == 1) {
            month = "0"+checkmonth[1];
        } else {
            month = checkmonth[1];
        }
        String year = String.valueOf(txtyearhireedit.getSelectedItem());

        if (!"".equals(txthourhireedit.getText()) && !"".equals(txtminishireedit.getText())) {
            Hire hire = new Hire();
            hire.setEmployee_id(Integer.parseInt(driverChoose[0]));
            hire.setStart_date(date+"-"+month+"-"+year+" "+txthourhireedit.getText()+":"+txtminishireedit.getText());
            hire.setVehice_id(Integer.parseInt(vehiceChoose[0]));
            hire.setId_hire(hire_id.getHire_id());
            QuerryHire.update(hire);
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Hãy điền đầy đủ thông tin","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

