    private void txthourhireeditKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txthourhireeditKeyPressed
        String minis = txthourhireedit.getText();
        String regex = "^[0-9]{2}$";
        Pattern pattern = Pattern.compile(regex, Pattern.MULTILINE);
        Matcher matcher = pattern.matcher(minis);
        if (matcher.find()) {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập số","Lỗi",JOptionPane.ERROR_MESSAGE);
            txthourhireedit.setText("");
        }
    }//GEN-LAST:event_txthourhireeditKeyPressed

    private void txthourhireeditKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txthourhireeditKeyReleased
        String minis = txthourhireedit.getText();
        if (minis.length() > 2) {
            txthourhireedit.setText("23");
        } else if (minis.length() == 2 ) {
            if (Integer.parseInt(minis) >= 24) {
                txthourhireedit.setText("23");
            }
        }
    }//GEN-LAST:event_txthourhireeditKeyReleased

