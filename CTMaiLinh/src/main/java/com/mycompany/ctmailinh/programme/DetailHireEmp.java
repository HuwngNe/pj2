    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int option = JOptionPane.showConfirmDialog(rootPane,
                        "Bạn có chắc muốn xác nhận đơn này không ?",
                        "Xác nhận",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
        if (option == 0) {
            QuerryHire.updateCorrect(hire_id.getHire_id(), idlogin.getId());
            JOptionPane.showMessageDialog(rootPane, "Xác nhận thành công");
            showData();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int option = JOptionPane.showConfirmDialog(rootPane,
                        "Bạn có chắc muốn xác nhận khách đã lấy xe không ?",
                        "Xác nhận",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
        if (option == 0) {
            QuerryHire.updateHire(hire_id.getHire_id(), idlogin.getId());
            QuerryVehice.updateStatus(iddata.getVehiceEmp_id(), 2);
            JOptionPane.showMessageDialog(rootPane, "Xác nhận thành công");
            showData();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

