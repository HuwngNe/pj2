    private void showData() {
        Hire checkNull = QuerryHire.findById(hire_id.getHire_id());
        Hire h = QuerryHire.findDetailEmp(hire_id.getHire_id(), checkNull.getEmployee_id());
        txtnamevehice.setText(h.getNameVehi());
        if (checkNull.getEmployee_id() != 0) {
            txtnamedriver.setText(h.getNameEploy());
        } else {
            txtnamedriver.setText("");
        }
        txtnamecustomer.setText(h.getNameCus());
        statushire.setText(h.getNameHire());
        statuspay.setText(h.getNamePayment());
        statuscorr.setText(h.getNameCorr());
        start.setText(h.getStart_date());
        if (!"null".equals(h.getEnd_date())) {
            end.setText(h.getEnd_date());
        } else {
            end.setText("");
        }
        iddata.setVehiceEmp_id(h.getVehice_id());
        iddata.setDriverEmp_id(h.getEmployee_id());
        iddata.setCustomerEmp_id(h.getCustomer_id());
    }
