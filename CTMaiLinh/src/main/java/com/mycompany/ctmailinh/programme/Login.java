    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        String username = txtuser.getText();
        String password = new String(txtpass.getPassword());
        Account account = new Account(username,password);
        account = QuerryAccount.find(account);
        if (account == null) {
            JOptionPane.showMessageDialog(rootPane, "Kiểm tra lại thông tin tài khoản","Lỗi",JOptionPane.ERROR_MESSAGE);
        } else {
            IdLogin idlogin = new IdLogin();
            idlogin.setId(account.getId());
            switch (account.getPermission_id()) {
                case 2 -> {
                    Admin admin = new Admin();
                    admin.setVisible(true);
                    setVisible(false);
                }
                case 1 -> {
                    User user = new User();
                    user.setVisible(true);
                    setVisible(false);
                    idlogin.setIdCurrent(account.getCustomer_id());
                }
                default -> {
                    EmployeeAction ea = new EmployeeAction();
                    ea.setVisible(true);
                    setVisible(false);
                    idlogin.setIdCurrent(account.getEmployee_id());
                }
            }
        }
    }//GEN-LAST:event_btnloginActionPerformed

